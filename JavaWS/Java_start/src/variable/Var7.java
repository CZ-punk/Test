package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100;    // 정수
        double b = 10.5;    // 실수
        boolean c = true;   // 불리안, true or false 만 가능
        char d = 'A';   // 문자 하나
        String e = "Hello Java";    // 문자열, 문자열을 다루기 위한 특별한 타입

        // 우리가 직접 적은 100, 10.5, true, A, Hello Java 를 Literal 이라고 부른다.
        // 개발자가 직접 입력한 값을 의미.
        
        System.out.println(a);  // 해당 라인 복사 단축키
        System.out.println(b);  // window : ctrl + d
        System.out.println(c);  // mac : cmd + d
        System.out.println(d);
        System.out.println(e);

    }
}
